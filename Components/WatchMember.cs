using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Hwagain
{
    //监视成员
    public class WatchMember : Attribute
    {
        string description = "";
        public WatchMember() { }

        #region 说明

        public string 说明
        {
            get
            {
                return description;
            }
            set
            {
                description = value;
            }
        }
        #endregion
    }
}
